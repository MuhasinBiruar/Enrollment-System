        Teachers a = new Teachers();
        //SELECT s.subjid, s.subjcode, s.subjdesc, s.subjunits, s.schedule, (SELECT count(e.studid) 
        //from enroll e where s.subjid = e.subjid) as Students FROM subjects s WHERE subjid in (SELECT subid FROM assign WHERE tid = 3001);
        
