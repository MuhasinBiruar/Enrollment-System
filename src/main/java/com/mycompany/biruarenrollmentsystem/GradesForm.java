    public void showGrades(){
        Grades a = new Grades();
        
        int subId = 0;
        int row1 = jTable2.getSelectedRow();
        if(row1 != -1){
            subId = Integer.parseInt(jTable2.getValueAt(row1, 0).toString());
            System.out.println("ID: " + subId);
        }

        String query = String.format(
                "SELECT s.studid,s.name,"
                + "(SELECT g.Prelim FROM `%s`.grades g WHERE g.eid=e.eid) AS Prelim,"
                + "(SELECT g.Midterm FROM `%s`.grades g WHERE g.eid=e.eid) AS Midterm,"
                + "(SELECT g.Prefinal FROM `%s`.grades g WHERE g.eid=e.eid) AS Prefinal,"
                + "(SELECT g.Final FROM `%s`.grades g WHERE g.eid=e.eid) AS Final "
                + "FROM `%s`.students s,`%s`.enroll e "
                + "WHERE s.studid=e.studid AND e.subjid=%d;",
                a.db, a.db, a.db, a.db, a.db, a.db, subId
        );
        DefaultTableModel model = ((DefaultTableModel) jTable3.getModel());
        model.setRowCount(0);
        try{
            a.rs = a.st.executeQuery(query);
            while(a.rs.next()){
                String studid = a.rs.getString("studid");
                String name = a.rs.getString("name");
                String prel = a.rs.getString("Prelim") == null ? "" : a.rs.getString("Prelim").equalsIgnoreCase("null") ? "" : a.rs.getString("Prelim");
                String mid = a.rs.getString("Midterm") == null ? "" : a.rs.getString("Midterm").equalsIgnoreCase("null") ? "" : a.rs.getString("Midterm");
                String pref = a.rs.getString("Prefinal") == null ? "" : a.rs.getString("Prefinal").equalsIgnoreCase("null") ? "" : a.rs.getString("Prefinal");;
                String finalGrade = a.rs.getString("Final") == null ? "" : a.rs.getString("Final").equalsIgnoreCase("null") ? "" : a.rs.getString("Final");
                String[] items = {studid, name, prel, mid, pref, finalGrade};
                model.addRow(items);
            }
        } catch(SQLException ex){
            query = String.format(
                "SELECT s.studid,s.name,"
                + "(SELECT NULL FROM null g WHERE g.eid=e.eid) AS Prelim,"
                + "(SELECT NULL FROM `%s`.grades g WHERE g.eid=e.eid) AS Midterm,"
                + "(SELECT NULL FROM `%s`.grades g WHERE g.eid=e.eid) AS Prefinal,"
                + "(SELECT NULL FROM `%s`.grades g WHERE g.eid=e.eid) AS Final "
                + "FROM `%s`.students s,`%s`.enroll e "
                + "WHERE s.studid=e.studid AND e.subjid=%d;",
                a.db, a.db, a.db, a.db, a.db, a.db, subId
        );
            try{
            a.rs = a.st.executeQuery(query);
            while(a.rs.next()){
                String studid = a.rs.getString("studid");
                String name = a.rs.getString("name");
                String prel = a.rs.getString("Prelim");
                String mid = a.rs.getString("Midterm");
                String pref = a.rs.getString("Prefinal");
                String finalGrade = a.rs.getString("Final");
                String[] items = {studid, name, prel, mid, pref, finalGrade};
                model.addRow(items);
              }
            }
            catch(SQLException ex3){
                    System.out.println("Unable to display students enrolled ex: " + ex);
                    }
            
            System.out.println("Unable to display students enrolled ex: " + ex);
        }
        
    }
    
